def xor(a: bytes, b: bytes) -> bytes:
    return bytes([x^y for x,y in zip(a,b)])


def getkey() -> bytes:
    ky = bytes([0xeb, 0xb5, 0xb3, 0x1d, 0x4e, 0xd0, 0x25, 0x86, 0x9c, 0x8c, 0x23, 0x5e, 0xb1, 0xba, 0xa0, 0xf8, 0x3d, 0x4C, 0xf0, 0x7f, 0x54, 0x10, 0x3d, 0xed, 0x6f, 0xf5, 0x33, 0xb0, 0x52, 0x92, 0x98, 0x77, 0xb4, 0xd2, 0x86, 0x13, 0x58, 0x4e, 0x0f, 0x02, 0xd4, 0x55, 0x8c, 0x74, 0xcb, 0xe5, 0xa0, 0xf7, 0xaa, 0x59])
    out = bytearray(0x100)
    
    for n in range(0x100):
        out[n] = (n * 0x02 + ky[n % 0x32] + 0x14) & 0xFF
    
    return bytes(out)


